package com.github.anno4j.model.namespaces;

/**
 * Ontology Class for RuleML namespace of the SWRL.
 * See <a href="http://www.w3.org/2003/11/swrl">http://www.w3.org/2003/11/swrl</a>
 */
public class RULEML {

    /**
     * Textual representation of the namespace.
     */
    public static final String NS = "http://www.w3.org/2003/11/swrl#";

    /**
     * Refers to http://www.w3.org/2003/11/swrl#Imp
     */
    public static final String IMP = NS + "Imp";

    /**
     * Refers to http://www.w3.org/2003/11/swrl#body
     */
    public static final String BODY = NS + "body";

    /**
     * Refers to http://www.w3.org/2003/11/swrl#head
     */
    public static final String HEAD = NS + "head";
}
